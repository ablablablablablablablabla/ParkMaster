# Импортируем библиотеку для взаимодействия с операционной системой
import os

# Устанавливаем переменную окружения, чтобы TensorFlow использовал старый формат Keras.
# Это часто нужно для совместимости с моделями, выгруженными из Teachable Machine.
os.environ["TF_USE_LEGACY_KERAS"] = "1"

# Импортируем библиотеку NumPy для работы с массивами чисел (матрицами)
import numpy as np

# Импортируем библиотеку Keras (через tf_keras) для работы с нейросетями
import tf_keras as keras

# Импортируем функцию загрузки сохраненной модели
from tf_keras.models import load_model

# Импортируем инструменты для обработки изображений: Image (открытие) и ImageOps (операции типа обрезки)
from PIL import Image, ImageOps


# Объявляем функцию, которая принимает три пути к файлам: сама модель, файл с именами классов и картинка
def get_class(model_path, labels_path, image_path):
    # Настраиваем вывод NumPy: suppress=True отключает научную нотацию (пишет 0.001 вместо 1e-3)
    # Это нужно просто для удобства чтения чисел в консоли, если будем их печатать.
    np.set_printoptions(suppress=True)

    # Загружаем "мозг" (нейросеть) из файла .h5.
    # compile=False означает, что мы не будем дообучать модель, только использовать.
    model = load_model(model_path, compile=False)

    # Открываем текстовый файл с метками классов (labels.txt) и читаем его построчно в список.
    # encoding="utf-8" важен, чтобы корректно читать кириллицу или спецсимволы.
    class_names = open(labels_path, "r", encoding="utf-8").readlines()

    # Создаем пустой NumPy массив (контейнер) определенной формы для подачи в нейросеть.
    # Форма (1, 224, 224, 3) означает: (Количество картинок, Высота, Ширина, Цветовые каналы RGB).
    # Нейросеть всегда ждет "пачку" картинок, даже если картинка всего одна.
    data = np.ndarray(shape=(1, 224, 224, 3), dtype=np.float32)

    # Открываем изображение по указанному пути и принудительно конвертируем в RGB.
    # Это важно: если картинка PNG, у нее может быть 4-й канал (прозрачность), который сломает модель.
    image = Image.open(image_path).convert("RGB")

    # Задаем целевой размер картинки, на котором обучалась модель (обычно 224x224).
    size = (224, 224)

    # "Умное" изменение размера: ImageOps.fit обрезает картинку по центру, сохраняя пропорции,
    # чтобы не сплющить и не растянуть объект. LANCZOS — это алгоритм сглаживания (фильтр).
    image = ImageOps.fit(image, size, Image.Resampling.LANCZOS)

    # Превращаем картинку (объект PIL) в массив чисел (матрицу NumPy).
    # Теперь картинка — это набор чисел от 0 до 255.
    image_array = np.asarray(image)

    # Нормализация данных: превращаем числа от 0 до 255 в диапазон от -1 до 1.
    # Формула: (X / 127.5) - 1. Нейросети работают лучше и быстрее с маленькими числами.
    normalized_image_array = (image_array.astype(np.float32) / 127.5) - 1

    # Кладем нашу подготовленную картинку в контейнер 'data', который мы создали ранее.
    # Индекс [0] означает, что мы кладем её на первое место в "пачке".
    data[0] = normalized_image_array

    # Самый главный момент: модель делает предсказание.
    # Результат (prediction) — это массив вероятностей для каждого класса (например: [0.1, 0.9, 0.0]).
    prediction = model.predict(data)

    # Находим индекс (номер) самого большого числа в массиве предсказаний.
    # Например, если массив [0.1, 0.9, 0.0], то argmax вернет 1 (так как 0.9 стоит под индексом 1).
    index = np.argmax(prediction)

    # Берем название класса из списка по найденному индексу.
    class_name = class_names[index]

    # Получаем саму цифру уверенности (вероятности) для победившего класса.
    confidence_score = prediction[0][index]

    # Возвращаем результат:
    # class_name[2:] — обрезаем первые 2 символа (обычно там цифра и пробел, например "0 Cat" -> "Cat").
    # confidence_score — уверенность модели (число от 0 до 1).
    return (class_name[2:].strip(), confidence_score)


# --- БЛОК ЗАПУСКА ---

if __name__ == "__main__":
    # 1. Указываем пути к файлам
    # Если файлы лежат в той же папке, просто пишем их названия
    model_file = "keras_model.h5"
    labels_file = "labels.txt"
    image_file = "file_1.jpg" # Замените на имя вашей картинки

    print(f"Загружаю модель из {model_file}...")
    print(f"Проверяю картинку {image_file}...")

    try:
        # 2. Вызываем функцию
        result_class, result_score = get_class(model_file, labels_file, image_file)

        # 3. Красиво выводим результат
        print("-" * 30)
        print(f"РЕЗУЛЬТАТ:")
        print(f"Это: {result_class}")
        # Превращаем число 0.985 в 98.5%
        print(f"Уверенность: {result_score * 100:.2f}%")
        print("-" * 30)

    except FileNotFoundError as e:
        print(f"\nОШИБКА: Не найден файл! {e}")
        print("Убедитесь, что файлы keras_model.h5, labels.txt и картинка лежат в папке с кодом.")
    except Exception as e:
        print(f"\nПРОИЗОШЛА ОШИБКА: {e}")
